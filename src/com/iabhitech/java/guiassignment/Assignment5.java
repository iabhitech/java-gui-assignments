    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        if (comboBoxSelectItem.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Please select a Item first!");
            return;
        }
        String item = comboBoxSelectItem.getSelectedItem().toString();
        for (int i = 0; i < comboBoxSelectedItem.getItemCount(); i++) {
            if (comboBoxSelectedItem.getItemAt(i).equals(item)) {
                JOptionPane.showMessageDialog(null, "Item Already in Selected Item", "Failed", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        comboBoxSelectedItem.addItem(item);
        JOptionPane.showMessageDialog(null, "Item Added!");
    }//GEN-LAST:event_btnAddItemActionPerformed

